package Lesson2;

public class CharDemo {
    public static void main(String[] args) {

        char ch;

        ch = 'X';
        int ascii = ch;

        System.out.println("ch contains " + ascii);

        ch++;

        System.out.println("Now ch contains: " + ch);

        ch = 90;

        System.out.println("Now ch contains: " + ch);
    }
}
